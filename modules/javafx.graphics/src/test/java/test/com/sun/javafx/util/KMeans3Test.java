    private static KMeans3.ClusterResult cluster(double[] points, int pointCount, int clusterCount) {
        return KMeans3.cluster(points, pointCount, clusterCount, 50, 0.001);
    }

    private static int[] recount(double[] points, int pointCount, double[] centers, int clusterCount) {
        int[] counts = new int[clusterCount];

        for (int i = 0; i < pointCount; i++) {
            int pointBase = i * 3;
            int best = nearestCenter(
                points[pointBase], points[pointBase + 1], points[pointBase + 2],
                centers, clusterCount);
            counts[best]++;
        }

        return counts;
    }

    private static int nearestCenter(double x, double y, double z, double[] centers, int clusterCount) {
        int best = 0;
        double bestDistance = distanceSq(x, y, z, centers, 0);

        for (int c = 1; c < clusterCount; c++) {
            double distance = distanceSq(x, y, z, centers, c);
            if (distance < bestDistance) {
                bestDistance = distance;
                best = c;
            }
        }

        return best;
    }

    private static double distanceSq(double x, double y, double z, double[] centers, int centerIndex) {
        int centerBase = centerIndex * 3;
        double dX = x - centers[centerBase];
        double dY = y - centers[centerBase + 1];
        double dZ = z - centers[centerBase + 2];
        return dX * dX + dY * dY + dZ * dZ;
    }

    private static void sortCentersByX(double[] centers) {
        if (centers[0] > centers[3]) {
            swapCenter(centers, 0, 3);
        }
    }

    private static void swapCenter(double[] centers, int firstBase, int secondBase) {
        for (int i = 0; i < 3; i++) {
            double temp = centers[firstBase + i];
            centers[firstBase + i] = centers[secondBase + i];
            centers[secondBase + i] = temp;
        }
    }

    private static void assertCenterEquals(double[] centers, int base,
                                           double x, double y, double z, double delta) {
        assertEquals(x, centers[base], delta);
        assertEquals(y, centers[base + 1], delta);
        assertEquals(z, centers[base + 2], delta);
    }
